
// Exempel på variabler och datatyper i C#

// Heltal
int heltal = 42;
long stortHeltal = 1234567890L;
short litetHeltal = -32000;
byte litetPositivtHeltal = 255;

// Flyttal
float flyttal = 3.14f;
double dubbelPrecision = 2.718281828;
decimal exaktTal = 19.99m;

// Tecken och strängar
char bokstav = 'A';
string text = "Hej, världen!";

// Booleska värden
bool sant = true;
bool falskt = false;

// Skriv ut alla variabler
Console.WriteLine($"int: {heltal}");
Console.WriteLine($"long: {stortHeltal}");
Console.WriteLine($"short: {litetHeltal}");
Console.WriteLine($"byte: {litetPositivtHeltal}");
Console.WriteLine($"float: {flyttal}");
Console.WriteLine($"double: {dubbelPrecision}");
Console.WriteLine($"decimal: {exaktTal}");
Console.WriteLine($"char: {bokstav}");
Console.WriteLine($"string: {text}");
Console.WriteLine($"bool (sant): {sant}");
Console.WriteLine($"bool (falskt): {falskt}");







